<?php
//単純な代入
//$numberに10を代入
$number = 10;

//複合代入演算子
$number += 5;//$numberに5を加える ( $number = $number + 5と同じ ) 15
$number -= 3;//$numberから3を引く ( $number = $number - 3と同じ ) 12
$number *= 2;//$numberを2倍する ( $number = $number * 2と同じ ) 24
$number /= 4;//$numberを4で割る ( $number = $number / 4と同じ ) 6
$number %= 3;//$numberを3で割った余りを代入 ( $number = $number % 3と同じ ) 0

//結果を出力
echo $number;//出力:0
?>